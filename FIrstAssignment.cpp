#include <iostream>

using namespace std;

int main() {
  int num1, num2, sum;

  // Prompt the user to enter two numbers
  cout << "Enter the first number: ";
  cin >> num1;

  cout << "Enter the second number: ";
  cin >> num2;

  // Calculate the sum of the two numbers
  sum = num1 + num2;

  // Output the sum to the user
  cout << "The sum of the two numbers is: " << sum << endl;

  return 0;
}
